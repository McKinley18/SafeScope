import { Injectable } from '@nestjs/common';

// pdfkit uses CommonJS export. Require is safer for runtime on Render.
const PDFDocument = require('pdfkit');

@Injectable()
export class PdfService {
  generateExecutivePdf(data: any): Promise<Buffer> {
    return new Promise((resolve, reject) => {
      try {
        const doc = new PDFDocument({ margin: 50 });
        const chunks: Buffer[] = [];

        doc.on('data', (chunk) => chunks.push(Buffer.from(chunk)));
        doc.on('error', reject);
        doc.on('end', () => resolve(Buffer.concat(chunks)));

        doc.fontSize(22).text('Sentinel Safety Executive Report', {
          align: 'center',
        });

        doc.moveDown();

        doc.fontSize(11).text(`Report ID: ${data.reportId}`);
        doc.text(`Generated: ${new Date(data.generatedAt).toLocaleString()}`);

        doc.moveDown();

        doc.fontSize(14).text('Inspection Summary', { underline: true });
        doc.moveDown(0.5);

        doc.fontSize(11).text(`Total Findings: ${data.totalFindings}`);
        doc.text(`High-Risk Findings: ${data.highRiskFindings}`);
        doc.text(`Dominant Hazard: ${String(data.dominantHazard || 'Unknown').replace(/_/g, ' ')}`);

        doc.moveDown();

        doc.fontSize(14).text('Executive Summary', { underline: true });
        doc.moveDown(0.5);

        doc.fontSize(11).text(data.summary || 'No executive summary available.', {
          align: 'left',
          lineGap: 4,
        });

        doc.moveDown();

        doc.fontSize(9).fillColor('#666666').text(
          'Generated by Sentinel Safety / SafeScope. Suggested standards and corrective actions are provided to support review. Final compliance determinations remain with qualified safety personnel.',
        );

        doc.end();
      } catch (err) {
        reject(err);
      }
    });
  }
}
